#[derive(Copy, Clone)]
struct Point {
    x: f64,
    y: f64,
    z: f64
}

impl Add for Point {
    type Output = Point;
    fn add(self, other: Point) -> Point {
        Point{ x: self.x + other.x, y: self.y + other.y, z: self.z + other.z }
    }
}
impl Sub for Point {
    type Output = Point;
    fn sub(self, other: Point) -> Point {
        Point{ x: self.x - other.x, y: self.y - other.y, z: self.z - other.z }
    }
}
impl Mul<f64> for Point {
    type Output = Point;
    fn mul(self, s: f64) -> Point {
        Point{ x: self.x * s, y: self.y * s, z: self.z * s }
    }
}

fn paper(p: Point) -> (u16, u16) {
    (((p.x + 1.0) / 2.0 * 7650.0 + 1000.0) as u16, ((-p.y + 1.0) / 2.0 * 7650.0) as u16)
}

fn canvas(p: Point) -> (f64, f64) {
    (((p.x + 1.0) / 2.0 * 1000.0), ((-p.y + 1.0) / 2.0 * 1000.0))
}

fn screen(p: Point) -> (u16, u16) {
    (((p.x + 1.0) / 2.0 * 96.0) as u16, ((-p.y + 1.0) / 2.0 * 38.0) as u16)
}

fn t2(p: Point) -> Point {
    Point { x: p.x, y: p.y + 0.25, z: p.z }
}

fn project(p: Point) -> Point {
    Point { x: p.x / (p.z / 2.0), y: p.y / (p.z / 2.0), z: p.z }
}

fn translate(p: Point) -> Point {
    Point { x: p.x, y: p.y - 0.9, z: p.z + 5.0 }
}

fn scale(p: Point) -> Point {
    let s = 2.0;
    Point { x: p.x * s, y: p.y * s, z: p.z * s }
}

fn rotate(p: Point, angle: f64) -> Point {
    let c = angle.cos();
    let s = angle.sin();
    Point { x: p.x * c - p.z * s, z: p.x * s + p.z * c, y: p.y }
}


