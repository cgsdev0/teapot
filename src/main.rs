use std::fmt::Display;
use std::ops::{Sub,Mul,Add};
fn set((x, y): (u16, u16), frame: &mut [u8]) {
    if y >= 38 { return; }
    if x >= 96 { return; }
    let byte = (y * 96 + x) / 8;
    let bit = 7 - x % 8;
    frame[byte as usize] |= 1 << bit;
}

